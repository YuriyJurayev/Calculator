package kz.epam.tam.module3.calculator;

public class Calculator {

    public int sum(int a, int b){
        return a + b;
    }

    public int sub(int a, int b){
        return a - b;
    }

    public  int mult(int a, int b){
        return a * b;
    }

    public  int div(int a, int b){
        return a / b;
    }
}
